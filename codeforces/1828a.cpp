#include<bits/stdc++.h>
using namespace std;
int main()
{
    int tt; cin >> tt;
    while (tt--) {
        int n; cin >> n;
        for (int i = 1; i <= n; i++) {
            cout << 2 * i << " ";
        }
        cout << "\n";
    }
    return 0;
}

